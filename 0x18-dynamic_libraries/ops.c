/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
